from typing import List
from collections import deque

class Solution:

    def num2index(self, i, n):
        i -= 1
        r = n - 1 - i // n
        c = i % n
        if i // n % 2 != 0:
            c = n - 1 - c
        return (r, c)
    
    def index2num(r, c):
        pass

    def snakesAndLadders(self, board: List[List[int]]) -> int:
        n = len(board)
        visited = set()
        line = deque([1])
        visited.add(1)
        steps = 1
        while line:
            for _ in range(len(line)):
                curr = line.popleft()
                for step in range(1, 7):
                    if curr + step > n * n:
                        break
                    r, c = self.num2index(curr + step, n)
                    if board[r][c] != -1:
                        des = board[r][c]
                    else:
                        des = curr + step
                    if des == n * n:
                        return steps
                    if des not in visited:
                        line.append(des)
                        visited.add(des)
            steps += 1
        return -1

